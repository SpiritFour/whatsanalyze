<template>
  <v-container ref="afd" style="position: relative">
    <slot ref="slot2"></slot>
    <v-btn
      @click="share"
      color="#016f94"
      fab
      dark
      style="position: absolute; right: 0; top: 0"
      :loading="loading"
    >
      <v-icon size="35" v-if="canShare">mdi-share</v-icon>
      <v-icon size="35" v-else>mdi-download</v-icon>
    </v-btn>
  </v-container>
</template>

<script>
import { downloadBase64File } from "~/functions/utils";

export default {
  name: "Share",
  props: {
    imageName: {
      type: String,
      default: "whatsanalyze.png",
    },
  },
  data() {
    return {
      canShare:
        (navigator.share &&
          navigator.canShare &&
          navigator.canShare({
            files: [new File([], "image.png", { type: "image/png" })],
          })) ||
        false,
      loading: false,
    };
  },
  methods: {
    share() {
      let chartName = this.$slots.default[0].componentOptions.tag;
      if (this.canShare) {
        this.$gtag.event("download-graph", {
          event_category: "home",
          event_label: chartName + "-share-pressed",
          value: "1",
        });
        this.loading = true;
        this.$slots.default[0].child.$refs.canvas.toBlob((blob) => {
          navigator
            .share({
              title: "WhatsAnalze.com",
              text:
                "Have a look at this graph of our conversation, generated by whatsanalyze.com.",
              files: [
                new File([blob], chartName + "-" + this.imageName, {
                  type: "image/png",
                }),
              ],
            })
            .then(() => {
              this.loading = false;
              this.$gtag.event("download-graph", {
                event_category: "home",
                event_label: chartName + "-shared",
                value: "1",
              });
            })
            .catch(() => (this.loading = false));
        });
      } else {
        this.$gtag.event("download-graph", {
          event_category: "home",
          event_label: chartName + "-download-pressed",
          value: "1",
        });
        let a = this.$slots.default[0].child.$refs.canvas.toDataURL();
        downloadBase64File(a, chartName + "-" + this.imageName);
        this.$gtag.event("download-graph", {
          event_category: "home",
          event_label: chartName + "-downloaded",
          value: "1",
        });
      }
    },
  },
};
</script>

